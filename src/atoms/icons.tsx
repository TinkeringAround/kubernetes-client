// ===================================================================
interface Props {
  type: IconType | null
  button?: boolean
  selected?: boolean
  disabled?: boolean
  size?: string
  iconSize?: string
  margin?: string
  color?: TColor
  style?: React.CSSProperties
  onClick?: any
}

// ===================================================================
const Icon: FC<Props> = ({
  type,
  button = true,
  selected = false,
  disabled = false,
  size = '40px',
  iconSize = '60%',
  margin = '0',
  color = 'blue',
  style,
  onClick = null
}) => {
  const [hovered, setHovered] = useState<boolean>(false)

  return (
    <Fragment>
      {button ? (
        <div
          style={{
            // Sizing
            width: size,
            height: size,
            margin: margin,

            // Styling
            background: disabled
              ? 'transparent'
              : selected
              ? colors['blue']
              : hovered && onClick
              ? colors['hoverBlue']
              : 'transparent',
            borderRadius: 5,

            // View
            display: 'flex',
            justifyContent: 'center',
            alignItems: 'center',

            // Options
            cursor: onClick ? 'pointer' : 'inherit',
            transition: 'all 0.25s ease',
            ...style
          }}
          onClick={onClick}
          onMouseOver={() => {
            if (!hovered) setHovered(true)
          }}
          onMouseLeave={() => {
            if (hovered) setHovered(false)
          }}
        >
          <svg
            // @ts-ignore
            viewBox={type ? icons[type].viewBox : '0 0 0 0'}
            style={{
              // Sizes
              width: iconSize,
              height: iconSize,

              //@ts-ignore
              fill: disabled ? colors['grey'] : selected ? 'white' : colors[color],

              // View Settings
              verticalAlign: 'middle',
              transition: 'all 0.25s ease'
            }}
          >
            {// @ts-ignore
            type ? icons[type].path : null}
          </svg>
        </div>
      ) : (
        <svg
          // @ts-ignore
          viewBox={type ? icons[type].viewBox : '0 0 0 0'}
          style={{
            // Sizes
            width: size,
            height: size,

            //@ts-ignore
            fill: selected ? 'white' : colors[color],

            // View Settings
            verticalAlign: 'middle',
            transition: 'all 0.25s ease'
          }}
        >
          {// @ts-ignore
          type ? icons[type].path : null}
        </svg>
      )}
    </Fragment>
  )
}

export default Icon
